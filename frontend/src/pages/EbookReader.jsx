const CHARS_PER_PAGE = 8000; // Increased characters per page to fill the screen
function paginateText(text, charsPerPage = 5000) {
  if (!text) return [""];

  const pages = [];
  let start = 0;

  while (start < text.length) {
    let end = start + charsPerPage;

    if (end < text.length) {
      while (end > start && text[end] !== " ") {
        end--;
      }
    }

    pages.push(text.slice(start, end));
    start = end;
  }

  return pages;
}

function updateQuoteState(text, initialState) {
  let state = initialState;
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    if (char === "“" || char === "”" || char === '"') {
      state = !state;
    }
  }
  return state;
}

function splitIntoSentences(text) {
  if (!text) return [];
  // Split on punctuation followed by space or end of string, using lookbehind so we do not discard punctuation
  const candidates = text.split(/(?<=[.!?])\s+/);
  const sentences = [];
  let currentSentence = "";
  // Added common prefixes/abbreviations to ignore
  const abbreviations = /^(?:Mr|Mrs|Ms|Dr|Prof|Sr|Jr|Gen|Col|Capt|Lieut|St|Rev|Hon|Jan|Feb|Mar|Apr|Jun|Jul|Aug|Sep|Oct|Nov|Dec|vs)\.?$/i;

  for (const candidate of candidates) {
    if (!candidate) continue;

    if (currentSentence) {
      currentSentence += " " + candidate;
    } else {
      currentSentence = candidate;
    }

    const trimmed = currentSentence.trim();
    // Support non-breaking spaces as word delimiters too by replacing &nbsp; with standard space for word-splitting checks
    const words = trimmed.replace(/&nbsp;/g, " ").split(/\s+/);
    const lastWord = words[words.length - 1];

    if (abbreviations.test(lastWord)) {
      continue;
    }

    sentences.push(currentSentence);
    currentSentence = "";
  }

  if (currentSentence.trim()) {
    sentences.push(currentSentence);
  }

  return sentences;
}

/**
 * EbookReader – renders a book as paginated black‑on‑white pages.
 * It also supports collaborative reading rooms where participants can
 * see each other's presence (name + current page) and invite others via a token.
 *
 * Props:
 *   book   – book object containing at least `title`, `author`, `ebook_text`.
 *   client – API client generated by useClient (used for all backend calls).
 *   onBack – callback invoked when the user clicks the back button.
 */
export function EbookReader({ book, client, onBack }) {
  // ---- Core reading state -------------------------------------------------
  const [pages, setPages] = useState([]);
  const [pageIndex, setPageIndex] = useState(0);
  const pageRef = useRef(null);
  const measureRef = useRef(null);
  // ---- Collaborative room state -------------------------------------------
  const [showPanel, setShowPanel] = useState("room");
  const [rooms, setRooms] = useState([]);
  const [activeRoom, setActiveRoom] = useState(null);
  const [roomData, setRoomData] = useState(null);
  const [participants, setParticipants] = useState([]);
  const [inviteEmail, setInviteEmail] = useState("");
  const [inviteToken, setInviteToken] = useState("");
  const [inviteTokenInput, setInviteTokenInput] = useState("");

  // Notes / bookmarks UI state (unchanged from original implementation)
  const [noteBody, setNoteBody] = useState("");
  const [bookmarkLabel, setBookmarkLabel] = useState("");
  const [msg, setMsg] = useState("");

  // ---- Bookmarks state and operations ----
  const [bookmarks, setBookmarks] = useState([]);

  const loadBookmarks = async () => {
    if (!book) return;
    try {
      const data = await client.request(`/books/${book.id}/bookmarks`);
      setBookmarks(data || []);
    } catch (_) {}
  };

  useEffect(() => {
    loadBookmarks();
  }, [book?.id]);

  const currentPageLabel = `Page ${pageIndex + 1}`;
  const currentPageBookmark = bookmarks.find(
    (b) => b.location === currentPageLabel && (!activeRoom || b.room_id === activeRoom)
  );
  const isBookmarked = !!currentPageBookmark;

  const toggleCurrentPageBookmark = async () => {
    if (isBookmarked) {
      try {
        await client.request(`/bookmarks/${currentPageBookmark.id}`, { method: "DELETE" });
        loadBookmarks();
      } catch (e) {
        setMsg(e.message);
      }
    } else {
      try {
        await client.request("/bookmarks", {
          method: "POST",
          body: JSON.stringify({
            book_id: book.id,
            room_id: activeRoom || null,
            location: currentPageLabel,
            label: currentPageLabel,
          }),
        });
        loadBookmarks();
      } catch (e) {
        setMsg(e.message);
      }
    }
  };
  // ---- Highlights state and operations ----
  const [highlights, setHighlights] = useState([]);
  const [deleteTooltip, setDeleteTooltip] = useState(null);

  const loadHighlights = async () => {
    if (!book) return;
    try {
      const data = await client.request(`/books/${book.id}/highlights`);
      setHighlights(data || []);
    } catch (_) {}
  };

  useEffect(() => {
    loadHighlights();
  }, [book?.id]);

  useEffect(() => {
    if (!deleteTooltip) return;
    const handleDocClick = (e) => {
      if (!e.target.closest("[data-highlight-id]") && !e.target.closest(".delete-tooltip")) {
        setDeleteTooltip(null);
      }
    };
    const timeout = setTimeout(() => {
      document.addEventListener("click", handleDocClick);
    }, 0);
    return () => {
      clearTimeout(timeout);
      document.removeEventListener("click", handleDocClick);
    };
  }, [deleteTooltip]);

  const handleTextSelection = async () => {
    if (activeHighlightColor === "none") return;
    
    const selection = window.getSelection();
    if (!selection || selection.isCollapsed) return;
    
    const selectedText = selection.toString().trim();
    if (!selectedText) return;
    
    let anchorNode = selection.anchorNode;
    let container = anchorNode;
    while (container && container !== document.body) {
      if (container.nodeType === Node.ELEMENT_NODE && container.hasAttribute("data-block-index")) {
        break;
      }
      container = container.parentNode;
    }
    
    if (!container || container === document.body) {
      return;
    }
    
    const blockIndex = parseInt(container.getAttribute("data-block-index"), 10);
    const startCharOffsetAttr = parseInt(container.getAttribute("data-start-char-offset"), 10) || 0;
    
    const range = selection.getRangeAt(0);
    const preSelectionRange = range.cloneRange();
    preSelectionRange.selectNodeContents(container);
    preSelectionRange.setEnd(range.startContainer, range.startOffset);
    
    const rawStartOffset = preSelectionRange.toString().length;
    const rawLength = range.toString().length;
    
    const start_offset = startCharOffsetAttr + rawStartOffset;
    const end_offset = start_offset + rawLength;
    
    try {
      await client.request("/highlights", {
        method: "POST",
        body: JSON.stringify({
          book_id: book.id,
          room_id: activeRoom || null,
          block_index: blockIndex,
          start_offset: start_offset,
          end_offset: end_offset,
          color: activeHighlightColor,
          text: selectedText
        })
      });
      selection.removeAllRanges();
      loadHighlights();
    } catch (err) {
      console.error("Error creating highlight:", err);
    }
  };

  const handleReaderClick = (e) => {
    const highlightEl = e.target.closest("[data-highlight-id]");
    if (highlightEl) {
      const hlId = highlightEl.getAttribute("data-highlight-id");
      const hl = highlights.find(h => h.id === parseInt(hlId, 10));
      if (hl) {
        setDeleteTooltip({
          highlight: hl,
          x: e.clientX,
          y: e.clientY
        });
        return;
      }
    }
    setDeleteTooltip(null);
  };

  // Chapter index for navigation
  const [chapters, setChapters] = useState([]);
  const [showChapters, setShowChapters] = useState(false);
  const [isPaginating, setIsPaginating] = useState(true);

  const ebookText = book?.ebook_text ?? book?.ebook?.text ?? "";

  const [blocks, setBlocks] = useState([]);
  const { theme, warmth, fontFamily, fontSize, lineHeight, layoutMode, activeHighlightColor } = useReaderTheme();

  useEffect(() => {
    if (!book) return;

    const parsed = parseBook(book);

    setBlocks(parsed.blocks);
    setChapters(parsed.chapters);
    setPageIndex(book._initialPageIndex ?? 0);
  }, [book]);

  // ---- Keyboard Arrow Navigation ------------------------------------------
  useEffect(() => {
    const handleKeyDown = (e) => {
      if (layoutMode === "scroll") return;

      const activeEl = document.activeElement;
      if (
        activeEl &&
        (activeEl.tagName === "INPUT" ||
          activeEl.tagName === "TEXTAREA" ||
          activeEl.tagName === "SELECT" ||
          activeEl.isContentEditable)
      ) {
        return;
      }

      if (e.key === "ArrowLeft") {
        setPageIndex((p) => Math.max(0, p - 1));
      } else if (e.key === "ArrowRight") {
        setPageIndex((p) => Math.min(pages.length - 1, p + 1));
      }
    };

    window.addEventListener("keydown", handleKeyDown);
    return () => {
      window.removeEventListener("keydown", handleKeyDown);
    };
  }, [pages.length, layoutMode]);

  // ---- Load available rooms for the user ----------------------------------
  useEffect(() => {
    client
      .request("/rooms")
      .then(setRooms)
      .catch(() => { });
    // If a room was passed via navigation (book._joinRoomId), auto‑join it.
    if (book._joinRoomId) {
      joinRoom(book._joinRoomId);
      setShowPanel("notes");
    }
  }, []);

  // ---- When a room becomes active, fetch its details and start polling -----
  useEffect(() => {
    if (activeRoom) loadRoom();
  }, [activeRoom]);

  // ---- Poll participants every 5 seconds ----------------------------------
  useEffect(() => {
    if (!activeRoom) return undefined;
    const poll = async () => {
      try {
        const data = await client.request(`/rooms/${activeRoom}/presence`);
        setParticipants(data.participants || []);
      } catch (_) { }
    };
    poll(); // initial fetch
    const interval = setInterval(poll, 5000);
    return () => clearInterval(interval);
  }, [activeRoom]);

  // ---- Whenever the local page changes, push presence update --------------
  useEffect(() => {
    if (!activeRoom) return;
    client
      .request(`/rooms/${activeRoom}/presence`, {
        method: "POST",
        body: JSON.stringify({ page_index: pageIndex }),
      })
      .catch(() => { });
  }, [pageIndex, activeRoom]);

  // -----------------------------------------------------------------------
  function fitsOnPage(candidateBlocks) {
    if (!measureRef.current || !pageRef.current) return true;

    const measure = measureRef.current;

    measure.innerHTML = "";

    candidateBlocks.forEach((block) => {
      const el = document.createElement(block.type === "heading" ? "h2" : "p");

      el.innerHTML = formatBlockText(block.text, block.quoteOpenAtStart);

      if (block.type === "heading") {
        el.style.cssText = `
        text-align: center;
        font-size: ${Math.round(fontSize * 1.5)}px;
        font-weight: 700;
        margin: 32px 0 24px;
        letter-spacing: .03em;
        font-family: ${fontFamily};
      `;
      } else {
        el.style.cssText = `
        font-size: ${fontSize}px;
        line-height: ${lineHeight};
        margin: ${block.isContinuation ? "-18px 0 18px" : "0 0 18px"};
        text-align: justify;
        text-indent: ${block.isContinuation ? "0" : "2em"};
        font-family: ${fontFamily};
      `;
      }

      measure.appendChild(el);
    });

    return measure.scrollHeight <= pageRef.current.clientHeight;
  }

  function paginate(blocks) {
    const pages = [];
    const chapters = [];
    let currentPage = [];
    let quoteOpen = false;

    for (let i = 0; i < blocks.length; i++) {
      const block = blocks[i];
      if (block.type === "heading") {
        if (currentPage.length > 0) {
          pages.push({
            lines: currentPage,
          });
        }
        chapters.push({
          title: block.text,
          pageIndex: pages.length,
        });
        currentPage = [{ ...block, blockIndex: i, startCharOffset: 0 }];
        quoteOpen = false;
        continue;
      }

      // Split paragraph block into sentences
      const sentences = splitIntoSentences(block.text);

      let isFirstSentenceOfParagraph = true;
      let sentenceIndex = 0;
      let currentOffset = 0;

      while (sentenceIndex < sentences.length) {
        const rawSentence = sentences[sentenceIndex];
        const sentence = rawSentence.trim();
        if (!sentence) {
          currentOffset = block.text.indexOf(rawSentence, currentOffset) + rawSentence.length;
          sentenceIndex++;
          continue;
        }

        let sentenceStart = block.text.indexOf(rawSentence, currentOffset);
        if (sentenceStart === -1) {
          sentenceStart = currentOffset;
        }

        if (isFirstSentenceOfParagraph || currentPage.length === 0) {
          const isContinuation = !isFirstSentenceOfParagraph;
          const newBlock = {
            type: "paragraph",
            text: sentence,
            isContinuation: isContinuation,
            quoteOpenAtStart: quoteOpen,
            blockIndex: i,
            startCharOffset: sentenceStart,
          };

          const candidate = [...currentPage, newBlock];
          if (fitsOnPage(candidate)) {
            currentPage.push(newBlock);
            quoteOpen = updateQuoteState(sentence, quoteOpen);
            isFirstSentenceOfParagraph = false;
            currentOffset = sentenceStart + rawSentence.length;
            sentenceIndex++;
          } else {
            if (currentPage.length > 0) {
              pages.push({
                lines: currentPage,
              });
              currentPage = [];
            } else {
              // Fallback: sentence is too long for a single empty page
              const words = sentence.split(/\s+/);
              let start = 0;

              while (start < words.length) {
                let low = start;
                let high = words.length;
                let best = start;

                while (low <= high) {
                  const mid = Math.floor((low + high) / 2);
                  const chunkText = words.slice(start, mid).join(" ");
                  const test = [
                    ...currentPage,
                    {
                      type: "paragraph",
                      text: chunkText,
                      isContinuation: isContinuation || start > 0,
                      quoteOpenAtStart: quoteOpen,
                      blockIndex: i,
                      startCharOffset: sentenceStart + sentence.indexOf(chunkText),
                    },
                  ];

                  if (fitsOnPage(test)) {
                    best = mid;
                    low = mid + 1;
                  } else {
                    high = mid - 1;
                  }
                }

                if (best === start) best++;

                const chunkText = words.slice(start, best).join(" ");
                const newChunkBlock = {
                  type: "paragraph",
                  text: chunkText,
                  isContinuation: isContinuation || start > 0,
                  quoteOpenAtStart: quoteOpen,
                  blockIndex: i,
                  startCharOffset: sentenceStart + sentence.indexOf(chunkText),
                };
                
                currentPage.push(newChunkBlock);
                quoteOpen = updateQuoteState(chunkText, quoteOpen);
                start = best;

                if (start < words.length) {
                  pages.push({
                    lines: currentPage,
                  });
                  currentPage = [];
                }
              }
              isFirstSentenceOfParagraph = false;
              currentOffset = sentenceStart + rawSentence.length;
              sentenceIndex++;
            }
          }
        } else {
          const lastBlockIndex = currentPage.length - 1;
          const originalText = currentPage[lastBlockIndex].text;

          const testBlock = {
            ...currentPage[lastBlockIndex],
            text: originalText + " " + sentence,
          };

          const candidate = [...currentPage.slice(0, lastBlockIndex), testBlock];
          if (fitsOnPage(candidate)) {
            currentPage[lastBlockIndex] = testBlock;
            quoteOpen = updateQuoteState(sentence, quoteOpen);
            currentOffset = sentenceStart + rawSentence.length;
            sentenceIndex++;
          } else {
            pages.push({
              lines: currentPage,
            });
            currentPage = [];
            isFirstSentenceOfParagraph = false;
          }
        }
      }
    }

    if (currentPage.length) {
      pages.push({
        lines: currentPage,
      });
    }
    return {
      pages,
      chapters,
    };
  }

  useEffect(() => {
    if (!blocks.length) return;
    setIsPaginating(true);

    const result = paginate(blocks);

    setPages(result.pages);
    setChapters(result.chapters);

    setIsPaginating(false);
  }, [blocks]);

  useEffect(() => {
    const resize = () => {
      if (!blocks.length) return;

      const result = paginate(blocks);
      setPages(result.pages);
      setChapters(result.chapters);
    };

    window.addEventListener("resize", resize);

    return () => window.removeEventListener("resize", resize);
  }, [blocks]);

  useEffect(() => {
    if (!pageRef.current || !measureRef.current) return;

    measureRef.current.style.width = `${pageRef.current.clientWidth}px`;
  }, [pages]);

  async function loadRoom() {
    try {
      const data = await client.request(`/rooms/${activeRoom}`);
      setRoomData(data);
    } catch (e) {
      setMsg(e.message);
    }
  }





  async function createRoom() {
    try {
      const room = await client.request("/rooms", {
        method: "POST",
        body: JSON.stringify({
          book_id: book.id,
          name: `${book.title} reading room`,
        }),
      });
      setActiveRoom(room.id);
      const updated = await client.request("/rooms");
      setRooms(updated);
    } catch (e) {
      setMsg(e.message);
    }
  }

  async function joinRoom(id) {
    try {
      await client.request(`/rooms/${id}/join`, { method: "POST" });
      setActiveRoom(id);
    } catch (e) {
      setMsg(e.message);
    }
  }

  async function addNote(e) {
    e.preventDefault();
    try {
      await client.request(`/rooms/${activeRoom}/notes`, {
        method: "POST",
        body: JSON.stringify({
          location: `Page ${pageIndex + 1}`,
          body: noteBody,
        }),
      });
      setNoteBody("");
      await loadRoom();
    } catch (e) {
      setMsg(e.message);
    }
  }

  async function addBookmark(e) {
    e.preventDefault();
    try {
      await client.request(`/rooms/${activeRoom}/bookmarks`, {
        method: "POST",
        body: JSON.stringify({
          location: `Page ${pageIndex + 1}`,
          label: bookmarkLabel,
        }),
      });
      setBookmarkLabel("");
      await loadRoom();
    } catch (e) {
      setMsg(e.message);
    }
  }

  // Invite creation – returns a token that can be shared with another user.
  async function createInvite() {
    if (!inviteEmail) {
      setMsg("Email is required to create an invite");
      return;
    }
    try {
      const data = await client.request(`/rooms/${activeRoom}/invite`, {
        method: "POST",
        body: JSON.stringify({ email: inviteEmail }),
      });
      setInviteToken(data.invite_token);
      setMsg("Invite token generated – share it with the other reader");
    } catch (e) {
      setMsg(e.message);
    }
  }

  // Accept an invitation token received from another user.
  async function acceptInvite() {
    if (!inviteTokenInput) {
      setMsg("Invite token is required");
      return;
    }
    try {
      const resp = await client.request(`/rooms/invite/accept`, {
        method: "POST",
        body: JSON.stringify({ token: inviteTokenInput }),
      });
      // The server returns the room_id; we can set it as active.
      setActiveRoom(resp.room_id);
      setInviteTokenInput("");
      setMsg("Joined room via invite token");
    } catch (e) {
      setMsg(e.message);
    }
  }

  const bookRooms = rooms.filter(
    (r) => r.book_id === book.id || r.title === book.title,
  );
  const currentPage = pages[pageIndex] ?? { lines: [] };
  const totalPages = pages.length;
  return (
    <div className={`relative w-full transition-colors duration-200 ${
      theme === "dark" 
        ? "dark bg-zinc-900 text-zinc-100" 
        : "bg-white text-zinc-900"
    } ${layoutMode === "scroll" ? "min-h-screen h-auto overflow-y-visible" : "h-screen overflow-hidden"}`}>
      {/* Warmth overlay shade */}
      <div
        className="warmth-overlay fixed inset-0 pointer-events-none z-10 transition-opacity duration-200"
        style={{ opacity: warmth / 100 }}
      />

      <div className="flex h-full w-full relative">
        {/* Main reader area */}
        <div 
          onClick={handleReaderClick}
          onMouseUp={handleTextSelection}
          className="flex-1 flex justify-center items-center relative h-full"
        >
          <ReaderContent
            ref={pageRef}
            currentPage={currentPage}
            pages={pages}
            pageIndex={pageIndex}
            totalPages={totalPages}
            onPrevious={() => setPageIndex((p) => Math.max(0, p - 1))}
            onNext={() => setPageIndex((p) => Math.min(totalPages - 1, p + 1))}
            highlights={highlights}
            style={{
              visibility: isPaginating ? "hidden" : "visible",
            }}
          />

          {/* Visual bookmark indicator/button directly on the book page */}
          {!isPaginating && (
            <button
              onClick={toggleCurrentPageBookmark}
              className={`absolute right-1/2 translate-x-[370px] max-lg:right-10 max-lg:translate-x-0 top-8 z-30 p-2 cursor-pointer transition-all duration-200 group focus:outline-none ${
                isBookmarked
                  ? "text-zinc-900 dark:text-zinc-100 hover:text-zinc-700 dark:hover:text-zinc-300 scale-110"
                  : "text-zinc-300 dark:text-zinc-700 hover:text-zinc-500 dark:hover:text-zinc-400 hover:scale-105"
              }`}
              title={isBookmarked ? `Bookmarked: ${currentPageBookmark.label} (Click to remove)` : "Bookmark this page"}
            >
              <Bookmark
                size={26}
                fill={isBookmarked ? "currentColor" : "none"}
                className="stroke-2 transition-transform group-hover:translate-y-0.5"
              />
            </button>
          )}

          {isPaginating && (
            <div className="absolute inset-0 z-10 flex items-center justify-center bg-white dark:bg-zinc-950">
              <ReaderContentSkeleton />
            </div>
          )}
          <div
            ref={measureRef}
            style={{
              position: "fixed",
              left: "-9999px",
              top: 0,
              visibility: "hidden",
              width: pageRef.current?.clientWidth || 900,
              height: "auto",
              padding: "40px 55px",
              boxSizing: "border-box",
              fontFamily: fontFamily,
              fontSize: `${fontSize}px`,
              lineHeight: lineHeight,
            }}
          />
        </div>
      </div>

      <ChapterSidebar
        open={showChapters}
        chapters={chapters}
        pageIndex={pageIndex}
        onClose={() => setShowChapters(false)}
        onSelectChapter={(page) => {
          if (layoutMode === "scroll") {
            let blockIndex = 0;
            for (let i = 0; i < page; i++) {
              blockIndex += pages[i]?.lines?.length || 0;
            }
            const el = document.getElementById(`block-${blockIndex}`);
            if (el) {
              el.scrollIntoView({ behavior: "smooth" });
            }
          } else {
            setPageIndex(page);
          }
          setShowChapters(false);
        }}
        bookId={book.id}
        activeRoom={activeRoom}
        totalPages={totalPages}
        client={client}
        bookmarks={bookmarks}
        onBookmarksChanged={loadBookmarks}
        highlights={highlights}
        onDeleteHighlight={async (id) => {
          try {
            await client.request(`/highlights/${id}`, { method: "DELETE" });
            loadHighlights();
          } catch (e) {
            setMsg(e.message);
          }
        }}
        onSelectBlock={(blockIndex) => {
          let targetPage = 0;
          for (let p = 0; p < pages.length; p++) {
            const pageLines = pages[p]?.lines || [];
            if (pageLines.some(line => line.blockIndex === blockIndex)) {
              targetPage = p;
              break;
            }
          }
          if (layoutMode === "scroll") {
            const el = document.getElementById("block-" + blockIndex);
            if (el) el.scrollIntoView({ behavior: "smooth" });
          } else {
            setPageIndex(targetPage);
          }
        }}
      />

      <ReaderControls
        pageIndex={pageIndex}
        totalPages={totalPages}
        onPrevious={() => setPageIndex((p) => Math.max(0, p - 1))}
        onNext={() => setPageIndex((p) => Math.min(totalPages - 1, p + 1))}
        onOpenChapters={() => setShowChapters(true)}
        onBack={onBack}
      />

      {deleteTooltip && (
        <div
          className="delete-tooltip fixed z-50 px-3.5 py-2 bg-zinc-950 dark:bg-zinc-800 text-white text-xs font-semibold rounded-lg shadow-xl flex items-center gap-2.5 -translate-x-1/2 -translate-y-full mb-3 animate-fade-in border border-zinc-800 dark:border-zinc-700"
          style={{ top: deleteTooltip.y, left: deleteTooltip.x }}
        >
          <button
            onClick={async (e) => {
              e.stopPropagation();
              try {
                await client.request(`/highlights/${deleteTooltip.highlight.id}`, { method: "DELETE" });
                loadHighlights();
                setDeleteTooltip(null);
              } catch (e) {
                setMsg(e.message);
              }
            }}
            className="text-red-400 hover:text-red-300 font-bold cursor-pointer"
          >
            Remove
          </button>
        </div>
      )}
    </div>
  );
}